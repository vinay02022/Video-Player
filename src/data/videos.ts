export function findVideoBySlug(slug: string) {
  for (const cat of videoDataset.categories) {
    const video = cat.contents.find((v) => v.slug === slug);
    if (video) return { video, categorySlug: cat.category.slug };
  }
  return null;
}

export function getVideosByCategory(categorySlug: string): VideoItem[] {
  const cat = videoDataset.categories.find((c) => c.category.slug === categorySlug);
  return cat?.contents ?? [];
}
